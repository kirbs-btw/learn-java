public class Operators {
    public static void main(String[] args) {
        /*
            +
            -
            *
            /
            %
            ++ inc by one
            -- dec by one
        */

        int a = 5;
        int b = 10;

        System.out.println(a + b);
        System.out.println(a * b);
        System.out.println(a - b);
        System.out.println(a / b);
        System.out.println(a % b);
        System.out.println(++a);

        a += 5;

        System.out.println(a);
    }
}
